    private void jbnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnAgregarActionPerformed
        // TODO add your handling code here:

    try {
        if (txtCodigo.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El Código de la ubicación es obligatorio.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (txtRc.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El Consecutivo (RC) es obligatorio.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            Integer.parseInt(txtRc.getText().trim());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "El Consecutivo (RC) debe ser un número entero.", "Error de formato", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        Modelo.ClaseUbicaciones ubi = new Modelo.ClaseUbicaciones();
        int rutaSeleccionada = Integer.parseInt(cmbRuta.getSelectedItem().toString());
        ubi.setRuta(rutaSeleccionada);
        ubi.setRc(txtRc.getText().trim());
        ubi.setCodigo(txtCodigo.getText().trim());
        ubi.setCalle1(txtCalle1.getText().trim());
        ubi.setCalle2(txtCalle2.getText().trim());
        ubi.setSentido(cmbSentido.getSelectedItem().toString());
        ubi.setTipoUbicacion(cmbTipo.getSelectedItem().toString());
        ubi.setEstado("ACTIVA");
        if (dateInstalacion.getDate() != null) {
            ubi.setFechaInstalacion(new java.sql.Date(dateInstalacion.getDate().getTime()).toString());
        } else {
            ubi.setFechaInstalacion("");
        }
        Controlador.ControladorUbicaciones control = new Controlador.ControladorUbicaciones();

        if (idEdicion == -1) {
            if (control.registrarUbicacion(ubi)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Ubicación agregada en la Ruta " + rutaSeleccionada + " con el consecutivo " + ubi.getRc());
                limpiarFormulario();
            }
        } else {
            ubi.setIdUbicacion(idEdicion);
            if (control.actualizarUbicacion(ubi)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Ubicación actualizada correctamente.");
                new ReporteUbicaciones(userSesion).setVisible(true);
                this.dispose();
            }
        }

    } catch (Exception e) {
        // --- LA TRAMPA PARA EL CRASHEO SILENCIOSO ---
        javax.swing.JOptionPane.showMessageDialog(this, "¡Error oculto detectado!\nDetalle: " + e.toString(), "Crasheo atrapado", javax.swing.JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); // Esto va a pintar el error exacto en la consola de NetBeans
    }

    }//GEN-LAST:event_jbnAgregarActionPerformed

