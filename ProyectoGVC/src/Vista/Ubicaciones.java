    private void limpiarFormulario() {
        // 1. Vaciar las cajas de texto
        txtCodigo.setText("");
        txtRc.setText(""); // La nueva cajita de tu lógica estrella
        txtCalle1.setText("");
        txtCalle2.setText("");
        if (cmbTipo.getItemCount() > 0) {
            cmbTipo.setSelectedIndex(0);
        }
        if (cmbRuta.getItemCount() > 0) {
            cmbRuta.setSelectedIndex(0);
        }
        if (dateInstalacion != null) {
            dateInstalacion.setDate(null);
        }
        if (cmbSentido.getItemCount() > 0) {
        cmbSentido.setSelectedIndex(0);
        }
        idEdicion = -1;
        jbnAgregar.setText("Guardar"); 
        txtCodigo.requestFocus();
    }

