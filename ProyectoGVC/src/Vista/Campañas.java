    private void jbnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnAgregarActionPerformed
        // TODO add your handling code here:
        if (txtNombre.getText().trim().isEmpty() || txtDescripcion.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, llena el Nombre y la versión.", "Campos vacíos", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (dateInicio.getDate() == null || dateFin.getDate() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona las fechas de inicio y fin.", "Fechas faltantes", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // EXTRA: Validar que la fecha final no sea antes de la inicial
        if (dateFin.getDate().before(dateInicio.getDate())) {
            javax.swing.JOptionPane.showMessageDialog(this, "La fecha de finalización no puede ser antes que la de inicio.", "Error en fechas", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. Extraer textos
        String nombre = txtNombre.getText().trim();
        String descripcion = txtDescripcion.getText().trim();
        java.time.LocalDate fechaInicioLocal = new java.sql.Date(dateInicio.getDate().getTime()).toLocalDate();
        java.time.LocalDate fechaFinLocal = new java.sql.Date(dateFin.getDate().getTime()).toLocalDate();
        java.time.LocalDate hoy = java.time.LocalDate.now();

        String estadoCalculado = "";
        if (hoy.isBefore(fechaInicioLocal)) {
            estadoCalculado = "PLANEADA";
        } else if (hoy.isAfter(fechaFinLocal)) {
            estadoCalculado = "FINALIZADA";
        } else {
            estadoCalculado = "ACTIVA";
        }

        Modelo.ClaseCampania campania = new Modelo.ClaseCampania();
        campania.setNombre(nombre);
        campania.setDescripcion(descripcion);
        campania.setEstado(estadoCalculado);
        campania.setFechaInicio(java.sql.Date.valueOf(fechaInicioLocal).toString());
        campania.setFechaFin(java.sql.Date.valueOf(fechaFinLocal).toString());
        Controlador.ControladorCampanias control = new Controlador.ControladorCampanias();

        if (idEdicion == -1) {
            if (control.registrarCampania(campania)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Campaña registrada. Estado: " + estadoCalculado);
                limpiarFormularioCampania();
            }
        } else {
            campania.setIdCampania(idEdicion);
            if (control.actualizarCampania(campania)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Campaña actualizada. Nuevo estado: " + estadoCalculado);
               new ReporteCampania(userSesion).setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jbnAgregarActionPerformed

