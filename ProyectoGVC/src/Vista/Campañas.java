    private void limpiarFormularioCampania() {
        txtNombre.setText("");
        txtDescripcion.setText("");

        if (dateInicio != null) {
            dateInicio.setDate(null);
        }
        if (dateFin != null) {
            dateFin.setDate(null);
        }

        txtNombre.requestFocus();
    }

